public class Variables {
    public static void main(String args[])
    {
        byte b=5;
        short s=100;
        int i=300;
        float a=25.3f;
        char c='A';
        System.out.println(b);
        System.out.println(s);
        System.out.println(i);
        System.out.println(a);
        System.out.println(c);
    }
}
